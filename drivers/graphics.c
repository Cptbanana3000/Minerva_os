void graphics_init(void) {
}

void graphics_put_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (x >= graphics_mode.width || y >= graphics_mode.height) {
        return;
    }

    uint32_t offset = (y * graphics_mode.pitch) + x;
    graphics_mode.framebuffer[offset] = (uint8_t)(color & 0xFF);
}

void graphics_draw_line(uint32_t x0, uint32_t y0, uint32_t x1, uint32_t y1, uint32_t color) {
    int dx = (x1 > x0) ? (x1 - x0) : (x0 - x1);
    int dy = (y1 > y0) ? (y1 - y0) : (y0 - y1);
    int sx = (x0 < x1) ? 1 : -1;
    int sy = (y0 < y1) ? 1 : -1;
    int err = dx - dy;

    while (1) {
        graphics_put_pixel(x0, y0, color);
        if (x0 == x1 && y0 == y1) break;

        int e2 = 2 * err;
        if (e2 > -dy) {
            err -= dy;
            x0 += sx;
        }
        if (e2 < dx) {
            err += dx;
            y0 += sy;
        }
    }
}

void graphics_draw_rect(uint32_t x, uint32_t y, uint32_t width, uint32_t height, uint32_t color) {
    graphics_draw_line(x, y, x + width, y, color);
    graphics_draw_line(x + width, y, x + width, y + height, color);
    graphics_draw_line(x + width, y + height, x, y + height, color);
    graphics_draw_line(x, y + height, x, y, color);
}

void graphics_fill_rect(uint32_t x, uint32_t y, uint32_t width, uint32_t height, uint32_t color) {
    for (uint32_t j = y; j < y + height; j++) {
        for (uint32_t i = x; i < x + width; i++) {
            graphics_put_pixel(i, j, color);
        }
    }
}

void graphics_clear(uint32_t color) {
    graphics_fill_rect(0, 0, graphics_mode.width, graphics_mode.height, color);
}

void graphics_draw_char(uint32_t x, uint32_t y, char character, uint32_t color, uint32_t bg_color) {
    uint8_t index = (uint8_t)character;
    const uint8_t* glyph = font_8x8[index];

    for (uint32_t row = 0; row < 8; row++) {
        uint8_t byte = glyph[row];
        for (uint32_t col = 0; col < 8; col++) {
            uint8_t pixel_color = (byte & (0x80 >> col)) ? (uint8_t)(color & 0xFF) : (uint8_t)(bg_color & 0xFF);
            graphics_put_pixel(x + col, y + row, pixel_color);
        }
    }
}

void graphics_draw_string(uint32_t x, uint32_t y, const char* string, uint32_t color, uint32_t bg_color) {
    uint32_t offset_x = 0;

    while (*string != '\0') {
        graphics_draw_char(x + offset_x, y, *string, color, bg_color);
        offset_x += 8;
        string++;
    }
}

void graphics_flip(void) {
    uint8_t* fb = GRAPHICS_FRAMEBUFFER;
    for (uint32_t i = 0; i < GRAPHICS_WIDTH * GRAPHICS_HEIGHT; i++)
        fb[i] = back_buffer[i];
}

uint8_t* graphics_get_framebuffer(void) {
    return back_buffer;
}

uint32_t graphics_rgb(uint8_t r, uint8_t g, uint8_t b) {
    /* Simple 8-bit palette: use basic VGA colors for now */
    /* Approximate RGB to nearest VGA palette entry */
    if (r < 85 && g < 85 && b < 85) return 0;    /* Black */
    if (r > 170 && g < 85 && b < 85) return 4;   /* Red */
    if (r < 85 && g > 170 && b < 85) return 2;   /* Green */
    if (r > 170 && g > 170 && b < 85) return 6;  /* Yellow */
    if (r < 85 && g < 85 && b > 170) return 1;   /* Blue */
    if (r > 170 && g < 85 && b > 170) return 5;  /* Magenta */
    if (r < 85 && g > 170 && b > 170) return 3;  /* Cyan */
    if (r > 170 && g > 170 && b > 170) return 15; /* White */
    return 7;  /* Light gray default */
}
